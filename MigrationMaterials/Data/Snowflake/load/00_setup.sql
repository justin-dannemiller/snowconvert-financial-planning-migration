CREATE SCHEMA IF NOT EXISTS SNOWFLAKE_LEARNING_DB.PLANNING_STG;

CREATE STAGE IF NOT EXISTS SNOWFLAKE_LEARNING_DB.PLANNING_STG.LOAD_STAGE;

