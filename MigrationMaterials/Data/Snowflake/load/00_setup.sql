  TYPE = CSV
  FIELD_DELIMITER = ','
  SKIP_HEADER = 0 -- the CSV files are exported without headers
  FIELD_OPTIONALLY_ENCLOSED_BY = '"'
  NULL_IF = ('', 'NULL', 'null')
  EMPTY_FIELD_AS_NULL = TRUE
  TRIM_SPACE = TRUE;
